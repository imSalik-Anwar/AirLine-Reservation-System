    private void addFlightBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addFlightBActionPerformed
        // TODO add your handling code here:
        String flightID = flightIDField.getText();
        String arrival = arrivalField.getText();
        String duration = durationField.getText();
        String fare = fareField.getText();
        String flightName = flightNameField.getText();
        String departure = departureField.getText();
        String totalSeats = totalSeatsField.getText();
        
        try{
            Connection con;
            PreparedStatement pre;
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/airlineproject", "root", "1234");
            pre = con.prepareStatement("Insert Into flight(FlightID,FlightName,Arrival,Departure,Duration,Seats,Fare)Values(?,?,?,?,?,?,?)");
            pre.setString(1, flightID);
            pre.setString(2, flightName);
            pre.setString(3, arrival);
            pre.setString(4, departure);
            pre.setString(5, duration);
            pre.setString(6, totalSeats);
            pre.setString(7, fare);
            
            pre.executeUpdate();
            JOptionPane.showMessageDialog(null, "Flight Added");
            this.setVisible(false);
            
        } catch (SQLException | ClassNotFoundException ex){
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addFlightBActionPerformed

