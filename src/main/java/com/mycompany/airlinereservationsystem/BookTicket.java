    private void searchSourceToDestinationBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchSourceToDestinationBActionPerformed
        try {
            // TODO add your handling code here:
            String Source = source.getSelectedItem().toString();
            String Destination = destination.getSelectedItem().toString();
            
            Connection con;
            PreparedStatement pre;
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/airlineproject", "root", "1234");
            pre = con.prepareStatement("Select * From flight where Arrival = ? And Departure = ?");
            pre.setString(1, Source);
            pre.setString(2, Destination);
            
            ResultSet rs = pre.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            int cc = rsmd.getColumnCount();
            DefaultTableModel dftm = (DefaultTableModel) table.getModel();
            
               if (rs.next()) {
                do {
                    Vector v = new Vector();
                    for (int i = 1; i <= cc; i++) { // Start from 1, not 0
                        v.add(rs.getString("FlightID"));
                        v.add(rs.getString("FlightName"));
                        v.add(rs.getString("Duration"));
                        v.add(rs.getString("Seats"));
                    }
                    dftm.addRow(v);
                    } while (rs.next());
                } else {
                    JOptionPane.showMessageDialog(null, "No flights found");
                }
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(BookTicket.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchSourceToDestinationBActionPerformed

